package com.ss.builder.control.painting;

/**
 * The enum with list of painting inputs.
 *
 * @author JavaSaBr
 */
public enum PaintingInput {
    /**
     * Mouse primary editing input.
     */
    MOUSE_PRIMARY,
    /**
     * Mouse secondary editing input.
     */
    MOUSE_SECONDARY,
    /**
     * Mouse secondary with ctrl editing input.
     */
    MOUSE_SECONDARY_WITH_CTRL,
}
